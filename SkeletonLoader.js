function SkeletonLoader() {
    return (React.createElement("div", { className: "w-full max-w-2xl flex flex-col min-h-screen animate-in fade-in px-6" },
        React.createElement("div", { className: "flex-grow pt-32 pb-48 space-y-4" }, __spreadArray([], Array(12), true).map(function (_, i) { return (React.createElement("div", { key: i, className: "h-4 bg-white/5 rounded-full animate-pulse", style: { width: "".concat(80 + Math.random() * 20, "%") } })); }))));
}
